#!/usr/bin/env node
// Regenerates server/assets/og-image-fallback.ts — the last-resort static OG image the
// render pipeline (server/utils/og-image/pipeline.ts) serves if dynamic rendering fails
// or hasn't been provisioned yet in an environment (see scripts/upload-og-renderer-assets.mjs).
//
// The two assets it replaces, server/assets/og-image-part-1.ts and -part-2.ts, decoded to
// bytes with valid JPEG SOI/EOI markers but were NOT a decodable image (confirmed with
// both `sharp` and Pillow) — this script exists so that failure mode can't recur silently:
// it re-renders the fallback through the same satori+resvg pipeline used at request time
// and would fail loudly if that pipeline ever produced non-decodable output again.
//
// Usage: node scripts/generate-og-fallback-image.mjs

import satori from 'satori'
import { Resvg, initWasm } from '@resvg/resvg-wasm'
import { readFile, writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import path from 'node:path'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(__dirname, '..')

const WIDTH = 1200
const HEIGHT = 630

async function loadFont(file, weight) {
  const data = await readFile(path.join(repoRoot, 'node_modules/@fontsource/inter/files', file))
  return { name: 'Inter', data, weight, style: 'normal' }
}

async function main() {
  const wasmBytes = await readFile(path.join(repoRoot, 'node_modules/@resvg/resvg-wasm/index_bg.wasm'))
  await initWasm(wasmBytes)

  const fonts = [
    await loadFont('inter-latin-400-normal.woff', 400),
    await loadFont('inter-latin-700-normal.woff', 700),
  ]

  const tree = {
    type: 'div',
    props: {
      style: {
        display: 'flex',
        flexDirection: 'column',
        justifyContent: 'flex-end',
        width: WIDTH,
        height: HEIGHT,
        padding: 64,
        fontFamily: 'Inter',
        backgroundImage: 'linear-gradient(135deg, #1e1b4b, #4338ca)',
      },
      children: [
        {
          type: 'div',
          props: {
            style: { display: 'flex', width: 56, height: 56, borderRadius: 12, backgroundColor: '#a5b4fc' },
          },
        },
        {
          type: 'div',
          props: {
            style: { display: 'flex', color: '#ffffff', fontSize: 58, fontWeight: 700, marginTop: 28, maxWidth: 1000 },
            children: 'KrabiClaw',
          },
        },
        {
          type: 'div',
          props: {
            style: { display: 'flex', color: 'rgba(255,255,255,0.85)', fontSize: 28, marginTop: 16, maxWidth: 900 },
            children: 'The AI-powered website builder for local businesses.',
          },
        },
      ],
    },
  }

  const svg = await satori(tree, { width: WIDTH, height: HEIGHT, fonts })
  const resvg = new Resvg(svg, { fitTo: { mode: 'width', value: WIDTH } })
  const png = resvg.render().asPng()

  const base64 = Buffer.from(png).toString('base64')
  const outPath = path.join(repoRoot, 'server/assets/og-image-fallback.ts')
  await writeFile(
    outPath,
    `// Generated by scripts/generate-og-fallback-image.mjs — do not hand-edit.\n` +
      `// A valid, decodable 1200x630 PNG used as the OG image pipeline's last-resort fallback.\n` +
      `export default '${base64}'\n`,
  )
  console.log(`Wrote ${outPath} (${png.byteLength} bytes PNG, ${base64.length} base64 chars)`)
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
